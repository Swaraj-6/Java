
public class Datatypes {
    public static void main(String[] args){
        int roll_no = 18;
        char letter = 'S';
        float marks = 98.67f;
        double largeDecimalNumbers = 5726567.6166764;
        long largeInteger = 8571465496874L;
        boolean check = true;

        System.out.println("Integer is " + roll_no);
        System.out.println("Char is " + letter);
        System.out.println("Float is " + marks);
        System.out.println("Double is " + largeDecimalNumbers);
        System.out.println("Long is " + largeInteger);
        System.out.println("Boolean is " + check);
    }
}
